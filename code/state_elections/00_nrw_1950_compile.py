PARTY_NAMES = ['cdu', 'spd', 'fdp', 'kpd', 'z', 'dp', 'drp', 'rsf', 'srp', 'csab', 'unab']

def validate():
    errors = 0
    for row in DATA:
        wk, ev, nv, vv = row[0], row[1], row[2], row[3]
        parties = row[4:]
        psum = sum(parties)
        if psum != vv:
            print(f"  ERROR WK {wk}: party_sum={psum} != VV={vv} (diff={psum-vv})")
            errors += 1
    print(f"\nValidated {len(DATA)} WK: {errors} errors, {len(DATA)-errors} OK")

    for name, st in sorted(SUBTOTALS.items()):
        lo, hi = st['wk_range']
        wk_rows = [r for r in DATA if lo <= r[0] <= hi]
        vv_sum = sum(r[3] for r in wk_rows)
        if 'vv' in st and vv_sum != st['vv']:
            print(f"  {name} VV: {vv_sum} vs expected {st['vv']} (diff={vv_sum-st['vv']})")
        for i, pn in enumerate(PARTY_NAMES):
            p_sum = sum(r[4+i] for r in wk_rows)
            if pn in st and p_sum != st[pn]:
                print(f"  {name} {pn}: {p_sum} vs expected {st[pn]} (diff={p_sum-st[pn]})")

    kreis_data = {}
    for row in DATA:
        wk = row[0]
        k, t = WK_TO_KREIS[wk]
        key = (k, t)
        if key not in kreis_data:
            kreis_data[key] = [0]*14
        for i in range(14):
            kreis_data[key][i] += row[1+i]
    print(f"\n{len(DATA)} WK -> {len(kreis_data)} Kreise")
    return kreis_data

def output_csv(kd, path):
    party_cols = ['cdu','spd','fdp','kpd','zentrum','dp','drp','rsf','srp','csab','unabhaengige']
    hdr = ['name','type','election_year','eligible_voters','number_voters','invalid_votes','valid_votes'] + party_cols
    rows = []
    for (n, t), v in sorted(kd.items()):
        ev, nv, vv = v[0], v[1], v[2]
        inv = nv - vv
        # v[3:] = cdu, spd, fdp, kpd, z, dp, drp, rsf, srp, csab, unab
        parties = list(v[3:])
        # Replace 0 with empty string for cleaner CSV
        parties = [p if p > 0 else '' for p in parties]
        row_type = t if t != 'combined' else 'combined'
        rows.append([n, row_type, 1950, ev, nv, inv, vv] + parties)
    with open(path, 'w', newline='', encoding='utf-8') as f:
        w = csv.writer(f)
        w.writerow(hdr)
        w.writerows(rows)
    print(f"Wrote {len(rows)} rows to {path}")

